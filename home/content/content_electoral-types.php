        <h3>Le scrutin uninomial</h3>
    </div>
    <div class="card-body">
    <h4>Le scrutin le plus connu des français</h4>
    <p>Si vous habitez en France, vous connaissez probablement ce type de scrutin car c'est celui qui est utilisé pour les <em>élections présidentielles</em>. Qu'il comporte un ou deux tours, son mode de fonctionnement reste le même : </p>
    <ul>
        <li>L'organisateur du vote sélectionne autant de candidats qu'il souhaite.</li>
        <li>Le jour du vote, chaque électeur vote pour <em>un(e) seul(e)</em> candidat(e). C'est d'ailleurs de là que vient le mot "uninomial".</li>
        <li>Si c'est un scrutin à un seul tour, c'est le candidat ayant eu le plus de voix qui l'emporte.</li>
        <li>Si il y a plusieurs tous, l'organisateur des scrutins sélectionne les meilleurs candidats et les fait s'affronter lors d'un deuxième tour, jusqu'à obtenir un vainqueur.</li>
    </ul>
    <p>Attention, il peut y avoir plus de deux candidats au deuxième tour ! En général, on choisit soit un <em>seuil de vote</em> (les candidats passent au second tour s'ils ont plus d'un certain pourcentage des sufffrages exprimés), soit un <em>nombre de candidat</em> (il y a un certain nombre connu à l'avance de candidats qui passeront au tour suivant).</p>

    <h4>Pour quel type d'élections ?</h4>
    <p>Ce type de scrutin peut principalement servir à deux types d'élections :</p>
    <ul>
        <li>Pour élire un <em>vainqueur unique</em> : celui qui remporte le tour final est choisi comme représentant de l'ensemble des électeurs.</li>
        <li>Pour élire une <em>chambre de réprésentats</em> : on ne vote non pas pour un candidat ou une candidate, mais pour une liste électorale rattachée à un parti. Au prorata de la proportion de suffrages obtenus, chaque parti enverra les premiers candidats de sa liste au sein de la chambre des représentats finale.</li>
    </ul>

    <h4>Quels avantages ? Quels inconvénients ?</h4>
    <p>Les avantages et inconvénients dépendent fortement du type de système électoral choisi, mais nous pouvons tout de même vous donner quelques exemples pour que vous vous fassiez votre propre avis :</p>
    <p><em>Scrutin uninomial à un tour :</em></p>
    <ul>
        <li>Si vous élisez une liste chambre de représentats, un scrutin à un tour vous permets de <em>représenter honorablement les minorités.</em> En effet, chaque vote est écouté, puisque tous les partis (éventuellement à partir d'un certain seuil) gagneront des représentants si l'on vote pour eux.</li>
        <li>Si vous élisez un représentant unique, ce vote a quelques désavantages. En effet, les électeurs devront dès le premier tour faire le <em>"vote de raison"</em> et non le <em>"vote de coeur"</em>, c'est à dire qu'ils n'auront pas intérêt à voter pour un candidat qu'ils préfèrent, mais qui n'a aucune chance de gagner, et préféreront soutenir le candidat qu'ils préfèrent parmis ceux qui ont une chance.</li>
    </ul>
    <p><em>Scrutin uninomial à deux tours :</em></p>
    <ul>
        <li>Ce vote a l'avantage et l'inconvénient d'assurer une certaine stabilité politique. En effet, il <em>ne permet pas de bien représenter les minorités</em>, qui perdent très fréquemment au second tour.</li>
        <li>Si vous élisez un représentant unique, le fait d'avoir deux tours permet aux électeurs de vter réellement pour le candidat qu'ils préfèrent <em>(vote du coeur)</em>, et non celui qui a le plus de chance de passer.</li>
    </ul>
