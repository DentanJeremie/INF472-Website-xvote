<div class="card border-dark produitsPhares" data-aos="fade-up" data-aos-duration="1000">
    <div class="card-header text-black themeColorBackgroung themeColorContent">
        <h3>La méthode de Borda</h3>
    </div>
    <div class="card-body">
    
    <h4>Un scrutin peu connu</h4>
    <p>Parmis les différents types de scrutins qui existent, nous avons choisi de vous proposer la <em>Méthode de Borda</em> car elle présente de multiples avantages. Voici comment elle fonctionne :</p>
    <ul>
        <li>L'organisateur du vote sélectionne autant de candidats qu'il souhaite.</li>
        <li>Le jour du vote, chaque électeur classe tous les candidats possible, de celui qu'il préfère à celui qu'il aime le moins.</li>
        <li>L'idée est ensuite <em>d'attribuer des points aux différents candidats,</em> et celui ou celle qui a le plus de points remporte l'élection.</li>
        <li>Supposons qu'il y ait n candidats. Dans ce cas, chaque électeur rapporte n points au candidat qu'il ou elle préfère, n-1 point au deuxième... et 1 point au dernier.</li>
        <li>On compte alors les points de tous les candidats, et celui qui en a le plus remporte</li>
    </ul>

    <h4>Pour quel type d'élections ?</h4>
    Cette fois encore, on peut utiliser la méthode de Borda pour deux types d'élections :
    <ul>
        <li>Pour élire un <em>vainqueur unique</em> : celui qui remporte le plus de points est choisi comme représentant de l'ensemble des électeurs.</li>
        <li>Pour élire une <em>chambre de réprésentats</em> : on ne vote non pas pour un candidat ou une candidate, mais pour une liste électorale rattachée à un parti. Au prorata dedu nombre de points obtenus, chaque parti enverra les premiers candidats de sa liste au sein de la chambre des représentats finale.</li>
    </ul>

    <h4>Pourquoi utiliser la méthode de Borda ?</h4>
    <p>L'avantage principal de la méthode de Borda est qu'il permet de <em>concillier une bonne représentation des minorités et une certaine stabilité politique.</em> En effet, la distinction entre le "vote du coeur" et le "vote de raison" est en partie effacée : on peut par exemple classer en haut de sa liste un candidat minoritaire mais que l'on affectionne particulièrement, et en deuxième un candidat qui a plus de chances, pour lui donner beaucoup de points. </p>
    <p>Malgré tout, les candidats peuvent être amenés à ne <em>pas donner leurs préférences réelles</em> pour favoriser artificiellement certains candidats. Cependant, la méthode de Borda réalise un <em>bon compromis</em> entre représentation des minorités stabilité politique.</p>


