        <h3>Et la légitimité du vainqueur ?</h3>
    </div>
    <div class="card-body">

    <p>Ce paragraphe concerne les deux types de scrutins disponibles ici. Parlons un peu du <em>paradoxe de Condorcet.</em> Ce paradoxe décrit une situation où, par exemple avec trois candidats A, B et C :</p>
    <ul>
        <li>En cas de duel A vs B, ce serait A qui l'emporte ;</li>
        <li>En cas de duel B vs C, ce serait B qui l'emporte ;</li>
        <li>En cas de duel C vs A, ce serait C qui l'emporte ;</li>
    </ul>
    <p>Ainsi, si le scrutin uninomial permet, par exemple, au candidat A d'être élu, il <em>manque de légitimité</em> face aux autres candidats. Ce type de situation peut sembler artificielle, mais elle est bien réelle. Plus précisément, il peut exister des situations (les exemples sont techniques à trouver mais ils existent dans la réalité) où un candidat aurait gagné tous les duels, mais n'est pas élu par un scrutin uninomial à deux tours !</p>
    <p>Pour plus de détails, nous vous conseillons d'aller regarder la <em>page Wikipédia du paradoxe de Condorcet.</em></p>
